# variáveis são, basicamente, conteineres onde armazenamos informações para serem usadas posteriormente no código

# Tipos primitivos de variáveis em Python
numero = 2
print(type(numero))

decimal = 3.6
print(type(decimal))

texto = 'Duda'
print(type(texto))

boleano = True # Para valores booleanos, usamos True ou False (com T e F maiúsculos)
print(type(boleano))